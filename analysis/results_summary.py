"""
Generate summary tables in multiple formats (CSV, Markdown) from raw results.
"""

import csv
import os
import sys

sys.path.insert(0, os.path.dirname(__file__))
from plot_config import model_display_name, normalize_model_name

import numpy as np

MODEL_META = {
    "qwen3-embedding": {"params": "7.6B Q4", "dims": 4096, "max_tokens": "32K", "mrl": True},
    "qwen3-embedding_4b": {"params": "4B", "dims": 4096, "max_tokens": "32K", "mrl": True},
    "nomic-embed-text": {"params": "137M", "dims": 768, "max_tokens": "8K", "mrl": True},
    "mxbai-embed-large": {"params": "335M", "dims": 1024, "max_tokens": "512", "mrl": False},
    "bge-m3": {"params": "568M", "dims": 1024, "max_tokens": "8K", "mrl": False},
    "all-minilm": {"params": "22M", "dims": 384, "max_tokens": "256", "mrl": False},
    "snowflake-arctic-embed": {"params": "334M", "dims": 768, "max_tokens": "512", "mrl": False},
}


def generate_markdown_table(summary_path, output_path):
    """Generate a Markdown summary table."""
    if not os.path.exists(summary_path):
        print(f"Missing {summary_path}")
        return

    with open(summary_path) as f:
        rows = sorted(list(csv.DictReader(f)), key=lambda r: -float(r["best_f1"]))

    lines = [
        "# Embedding Model Benchmark Results",
        "",
        "| Rank | Model | Params | Dims | F1 | AUC | Threshold | Recall@0.9 |",
        "|------|-------|--------|------|----|----|-----------|------------|",
    ]

    for i, row in enumerate(rows, 1):
        model = row["model"]
        meta = MODEL_META.get(normalize_model_name(model), {})
        name = model_display_name(model)
        lines.append(
            f"| {i} | **{name}** | {meta.get('params', '?')} | "
            f"{meta.get('dims', '?')} | {float(row['best_f1']):.3f} | "
            f"{float(row['roc_auc']):.3f} | {float(row['best_threshold']):.2f} | "
            f"{float(row['recall_at_0.90']):.3f} |"
        )

    lines.extend([
        "",
        "## Key Findings",
        "",
        f"- **Best overall**: {model_display_name(rows[0]['model'])} "
        f"(F1={float(rows[0]['best_f1']):.3f})",
        f"- **Most efficient**: {model_display_name(rows[-1]['model'])} "
        f"(smallest model, fastest inference)",
        f"- **Optimal thresholds range**: "
        f"{min(float(r['best_threshold']) for r in rows):.2f} — "
        f"{max(float(r['best_threshold']) for r in rows):.2f} "
        f"(NOT 0.9 as commonly assumed)",
        "",
        "---",
        "*Generated by bugspotter-embedding-benchmark*",
    ])

    os.makedirs(os.path.dirname(output_path) or ".", exist_ok=True)
    with open(output_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))
    print(f"Markdown summary saved to {output_path}")


def main():
    summary_path = "results/raw/model_summary.csv"
    generate_markdown_table(summary_path, "results/RESULTS.md")


if __name__ == "__main__":
    main()
